#!/bin/bash

# กำหนดปลายทางย้ายเข้าโฟลเดอร์ระบบของ Termux ทันที
TARGET_DIR="$PREFIX/bin"

echo "=========================================="
echo "  กำลังติดตั้ง jpp compiler สำหรับ Termux ARM64"
echo "=========================================="

# 1. ตรวจสอบและดาวน์โหลดไฟล์ jpp ล่าสุดจาก GitHub Releases อัตโนมัติ
# (สคริปต์จะดึงไฟล์จากแท็กล่าสุดให้อัตโนมัติโดยที่คุณไม่ต้องมาคอยแก้เลข v1.0.0 ทุกครั้ง)
DOWNLOAD_URL="https://github.com"

echo "-> กำลังดาวน์โหลดไฟล์รันระบบ..."
if curl -sL -O "$DOWNLOAD_URL"; then
    echo "[✓] ดาวน์โหลดสำเร็จ"
else
    echo "[X] เกิดข้อผิดพลาด! ไม่พบไฟล์ในระบบ Release กรุณาตรวจสอบการอัปโหลด"
    exit 1
fi

# 2. ตั้งค่าสิทธิ์และติดตั้งเข้าระบบอัตโนมัติ
echo "-> กำลังตั้งสิทธิ์และย้ายไฟล์ไปยัง $TARGET_DIR..."
chmod +x jpp
mv jpp "$TARGET_DIR/"

echo "=========================================="
echo "    [✓] ติดตั้ง jpp สำเร็จเรียบร้อยแล้ว!"
echo "    ลองพิมพ์คำสั่ง 'jpp' เพื่อเริ่มต้นใช้งาน"
echo "=========================================="
